package main

import "fmt"

func main() {
	// Num
	fmt.Println("Satu =", 1)
	fmt.Println("Dua =", 2)
	fmt.Println("Tiga koma lima =", 3.5)

	// Bool
	fmt.Println(true)
	fmt.Println(false)

	// String
	fmt.Println("Raditya Dwisatrio")
	fmt.Println("Raditya Dwisatrio"[0])   // 82 (byte)
	fmt.Println(len("Raditya Dwisatrio")) // 17
}
